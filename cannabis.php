						<h2>
                                                    Adicción al Cannabis
						</h2>
						<p>El <em>cannabis</em> es una droga derivada de la planta del cáñamo (<em>Cannabis sativa</em>). Suele consumirse fumada en distintas presentaciones, las más frecuentes hachis y marihuana. <strong>Todas contienen las mismas sustancias adictivas, siendo la principal el Δ-9-tetrahidrocannabinol (THC). </strong></p>
                                                <p>Su efecto se debe a que el THC se parece a algunas de las sustancias que las neuronas producen para comunicarse entre sí. El THC se une a los receptores de estas neuronas alterando gravemente a su normal funcionamiento y, por tanto, el de nuestro cerebro.</p>
                                                <p>El THC del cannabis se acumula en los tejidos grasos del cuerpo y tarda unos 30 días en ser eliminado del organismo. El adicto al cannabis puede estar varios días sin consumir porque el THC se ha almacenado en el cerebro y la grasa corporal. Esto le hace creer erróneamente que “controla” cuando no es cierto. Raramente va a llegar al mes sin consumir.</p>
                                                <p>Cuando el consumo es semanal, y más aún, si es diario, podemos hablar claramente de enfermedad por adicción al cannabis.</p>
                                                <p><strong>EFECTOS TOXICOS DEL CANNABIS</strong></p>
                                                <p><strong> </strong></p>
                                                <p><strong> </strong></p>
                                                <p>- <strong>Adiccion</strong>: todos los estudios serios destacan el alto potencial adictivo del cannabis. El consumo modifica los circuitos dopaminérgicos del cerebro de  forma similar a otras sustancias adictivas como la cocaína o el alcohol. El abandono definitivo del consumo es difícil y por ello el adicto necesita ayuda profesional. Muchos pacientes se engañan pensando que “controlan”, cuando la realidad es que al final, poco o mucho, siguen consumiendo. La adicción es la enfermedad del autoengaño y el cannabis es la sustancia con la que el adicto vive más engañado.</p>
                                                <p>- <strong>Trastornos de la motivación  (“Síndrome Amotivacional del Cannabis”</strong>): Consiste en que el consumidor de cannabis desarrolla gradualmente una disminución del interés por actividades saludables: estudio, deporte, actividades y amistades sanas. Siente que los estudios, el trabajo, la familia, el ocio saludable y sus amigos sanos no le “llenan”. Sólo le atrae la pandilla con la que consume. Hace muchos planes, pero normalmente nunca los llega a realizar. Una sensación de vacío hace su vida desagradable. Solo el consumo alivia esa sensación. Poco a poco, su círculo de relaciones va quedando más y más limitado a sus “colegas” de consumo. Para paliar este efecto el adicto va aumentando el consumo de porros o incorporando otras sustancias como alcohol, anfetaminas, éxtasis, cocaína ,etc.</p>
                                                <p>- <strong>Trastornos de ansiedad</strong>: a pesar de su efecto relajante inicial, el adicto al cannabis termia necesitando aumentar más y más el consumo porque la ansiedad gradualmente empeora. De hecho, llega un momento que el cannabis puede llevar a la aparición de diversos trastornos de ansiedad como crisis de pánico, agorafobia o ansiedad flotante. En muchos casos esto hace que aumente el uso de otras sustancias sedantes como alcohol o tranquilizantes.</p>
                                                <p>- <strong>Trastornos psicóticos</strong>: También desde hace años se ha constatado que el consumo de cannabis puede desencadenar trastornos psicóticos con síntomas idénticos a los que producen la esquizofrenia y otras psicosis. Estos síntomas a veces desaparecen espontáneamente dejando de consumir y en otras persisten requiriendo entonces un tratamiento psiquiátrico prolongado.</p>
                                                <p>- <strong>Pérdida de memoria, déficit de atención  y falta de concentración</strong>: El efecto del cannabis en el cerebro del adicto hace que vayan deteriorando funciones cerebrales como la concentración, la memoria, la atención, disciplina, cumplimiento de horarios, alteración del horario del sueño, etc. Todo ello conlleva fracaso escolar, falta de rendimiento laboral, conflictos en casa y fuera de ella. El consumo de cannabis está muchas veces detrás del fracaso escolar y laboral de nuestros jóvenes. Estos efectos suelen mejorar o desaparecer a los pocos meses del abandono definitivo del consumo.</p>
                                                <p>- <strong>El cannabis como droga de inicio</strong>: Cannabis y alcohol suelen ser las primeras drogas que los jóvenes consumen. Son las que inician las alteraciones neurológicas que terminarán configurando la enfermedad de adicción. Iniciado el proceso adictivo, se suelen incorporar progresivamente otras sustancias: dosis elevadas de alcohol, anfetaminas y otros estimulantes, cocaína, etc. que consolidan y complican gravemente la adicción.</p>
                                                <p><strong>¿COMO ACTUAR?</strong></p>
                                                <p>Ante la sospecha de que hay problemas relacionados con consumo de sustancias adictivas, lo más importante es no perder de vista que puede terminar desarrollando una enfermedad de adicción al cannabis y, probablemente, a otras sustancias. Lo urgente es conseguir un <strong>diagnóstico profesional </strong>de la situación del paciente y, si es necesario, un tratamiento efectivo que la resuelva.</p>
                                                <p>En <strong>Instituto Castelao</strong>, ofrecemos el servicio de <strong>Visita Previa</strong> que permite valorar la situación del paciente. Tras esta visita el equipo facultativo de Instituto Hipócrates recomendará lo más pertinente en cada caso y asesorará a la familia y al paciente sobre cómo actuar.</p>
	
									
