						<h2>
                                                    La familia es una pieza clave para la recuperaci&oacute;n
						</h2>
						<p>
                                                    Con el paso de los años se pierde la esperanza de recuperar al adicto
                                                    o que la situaci&oacute;n vaya a mejor. Esta situaci&oacute;n es común en todos
                                                    los entornos de abuso de sustancias.
                                                </p>
                                                <p>
                                                    Los sentimientos y las reacciones que surgen de la convivencia con un adicto
                                                    suelen generar tensiones y episodios de conflicto en el entorno cotidiano y familiar.
                                                    Temor, inquietud, preocupaci&oacute;n, tristeza, enfado, sentimientos de culpa  y vergüenza son 
                                                    sentimientos que aparecen en quienes conviven día a día con un adicto.
                                                </p>
                                                <p>
                                                    Las terapias familiares y de pareja est&aacute;n enfocadas a abordar estas situaciones. Las terapias familiares y de pareja así como las entrevistas de los
                                                    familiares con los terapeutas tienen el objetivo de profundizar en el conocimiento de la enfermedad 
                                                    y los mecanismos de la recuperaci&oacute;n.
                                                    
