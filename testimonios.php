						<div id="madre-alcoholica-general" class="testimonio post">

					
                            <p>		<img src="images/responsive/mila.jpg" alt="Centros de desintoxicación" class="testim-image" />						
Primero fue un cáncer de mama, luego fue la muerte de mi esposo alcohólico con 44 años y finalmente, como si fuera un brindis muy cabrón, vino el vodka.
<br>Nada más recuperarme del cáncer que yo estaba sufriendo, mi pareja (que era bebedor) falleció. Entonces, al mes y medio, empecé a beber y a beber. Cuesta abajo. La gente me decía: "Pero Mila, ¿cómo bebes así?, si tú ya viste cómo acabó él". <b>Los alcohólicos terminamos siendo especialistas en autoengaño</b>. Te dices a ti misma: "Ha muerto Manuel, ¿cómo no voy a beber?". Y entonces llegaba a casa y me ponía la bata suya para sufrir y tener una coartada para emborracharme. La cosa llegó a un punto en que el psicólogo me terminó diciendo: "No te lo creerás, pero cada vez que piensas en él es porque tienes ganas de beber".
                                
                                </p>
<p> Antes iba por la mañana a los bares de mi barrio con una urgencia temprana. Como la que sospecha que algo pasó la noche anterior pero no recuerda qué. <b>Les decía a los camareros: "¿Os debo algo? ¿Me pasé mucho ayer? Os pido perdón"</b>. Era una bayeta que lo absorbía todo. Tirada por el suelo si hacía falta. </p>
                            
<p><b>Una acaba siendo un poco vampiro, no tanto por lo que suponía la noche, sino porque al final el consumo era con las persianas bajadas</b>. Una viuda calva; un salón a oscuras; un suelo lleno de botellas con las que te tropezabas; Y unos hijos en la ESO, huérfanos de padre, que llegaban a casa por la tarde con un montón de deberes. No les atendía, compraba muy poca comida y la acababa tirando porque no se la cocinaba. Sólo había fiambre y fiambre. Mi hermana veía el panorama y se los llevaba a comer a casa de ella. 
        
</p>
<p>Entonces salía, como la que tiene que ir a comprar al súper o la que tiene una lista de tareas muy urgente. "Voy a hacer unas cosas", decía. <b>Con los mellizos contestando en el papel de padre y de madre: "¿Otra vez, mamá?". Y otra vez que iba y no volvía</b>. </p>
<p>Yo como madre me sentía la mierda de las mierdas de las mierdas. Por lo mala que era. <b>Eso es lo más tremendo para nosotras: la culpa</b>. Hasta que comprendí que había que empezar por darle la vuelta al tema. Literalmente. 

Un día cogí una botella de champán y la vacié en el fregadero. Mientras tiraba el líquido, le dije a Manuel: "Te prometo que no vuelvo a beber". Tengo los ojos de mi hijo aquí clavados en mi mente, y su respuesta: "Mamá, eso ya me lo dijiste el otro día".
</p>
     
<p>  <strong> Pero esa vez lo decía de verdad (antes no) y desde ese día no he vuelto a probar el alcohol. El día que decidí llamar a Castelao hace ya cuatro años.</strong>
                            </p>
                            

     
