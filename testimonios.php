                            <p>

<i style="color:rgba(3,139,209,1)">Silvia ha querido compartir su experiencia como <strong>madre de una joven adicta recuperada</strong>, con el anhelo de que su testimonio sirva de ayuda a quienes, como ella, conocen de cerca una enfermedad capaz de destrozar familias. <br>

Reproducimos su relato bajo estas líneas; un relato de sufrimiento, pero también de esperanza e ilusiones renovadas. Y agradecemos profundamente la generosidad que emana de sus palabras.
</i>


</p>
                            
							<h3 class="testim">
								La luz al final del túnel
							</h3>
                            <p>“El 10 de marzo de 2016, completamente desesperada y hundida, busqué en internet centros de ayuda para la drogadicción. Encontré la página de Instituto Castelao y llamé. Me dieron cita para el día siguiente. 
Mi hija ingresó en el centro de Málaga apenas cinco días después. Fue exactamente el 15 de marzo de 2016: una fecha que nos ha devuelto a la vida y que jamás olvidaré.”
                            </p>
                        </div>              
                  <div id="familia-estructurada" class="testimonio post">
                      
                            <h3 class="testim">
								Una familia estructurada
							</h3>
                            <p>“La nuestra es una familia perfectamente estructurada. Mi hija tenía unos padres, dos hermanos, una abuela, tíos, primos,... Y un grupo de amigas del colegio estupendas. Es decir, <strong>tenía todo el apoyo emocional que pudiera necesitar</strong>. Pero nos saltaron las alarmas porque la suya no era una adolescencia normal… 
Un verano, con 14 o 15 años, nos dijo que necesitaba irse fuera de España para poner en orden sus ideas, su mente. Yo lo achaqué a la revolución hormonal y la mandamos a Dublín. Fue allí donde empezó a consumir. Primero hachís. Luego vendría todo lo demás.<br>
<br>

 
                                El caso es que nuestra vida se iba consumiendo poco a poco, día a día. ¿Por qué? ¡Si lo teníamos todo! Una familia maravillosa, trabajo, una casa perfecta… Pero también teníamos <strong>una hija con un grave problema de comportamiento al que no sabíamos ponerle nombre</strong>. Un problema que, seguramente, al principio, tampoco queríamos entender… Una niña maravillosa que con 16, 17 y 18 años de edad se había metido en un bucle del que no podía salir, aunque -he de reconocerlo- nosotros pensábamos que ella era la que no quería salir… Que le gustaban ese tipo de vida y ese tipo de compañías; que ya no le interesaba estudiar, ni la familia, ni sus amigas de siempre… ¡Qué equivocados estábamos! <strong>Nunca se nos pasó por la cabeza que lo que teníamos era una niña infeliz, pero, sobre todo, una niña enferma</strong>.”

</p>
                        </div>
                 <div id="mentira-constante" class="testimonio post">           
                             <h3 class="testim">La mentira constante</h3>
                            <p>“Yo intuía que mi hija consumía, sí; que fumaba porros, pero pensaba que como el tabaco –yo soy fumadora- podría dejarlo cuando quisiese. Hasta que no llegamos a este centro no tuve conciencia de lo que realmente es la enfermedad de la adicción.<br>
Un mensaje de Whatsapp -que leí en su móvil y en el que alguien le preguntaba si quería consumir aquella tarde- me puso sobre la pista. A partir de ahí, empecé a hacerle un seguimiento exhaustivo.<br>
Fue un calvario.<br>
                                Mi hija era una pura mentira, un mentira constante. <strong>Mentía y mentía para ocultar su adicción</strong>. Llegó un momento en que empezamos a hacerle test de orina, que siempre daban positivo y revelaban que había consumido, por lo menos, cannabis, que es una droga que deja rastro en sangre durante varios días…. Pero ella seguía mintiendo.<br>
Un año antes de ingresar en Castelao, busqué ayuda en otro centro. Allí me dijeron que debía ingresarse, aparcar por completo su vida, dejar el colegio, los estudios, las amigas, el móvil,... Como así ha ocurrido después en Instituto Castelao. <br>
Pero en aquel momento, yo me resistí. Erróneamente, pensé que habría una fórmula intermedia -sin tener que llegar a ingresarla- y que podíamos intentarlo con la ayuda de psicólogos. Le dieron medicación, se sometió a varios tratamientos. Llegó a estar hasta tres semanas sin consumir. Entonces -¡ingenua de mí!-, creí que el problema ya estaba resuelto. Pero volvió a caer.”
</p>
                        </div>
                      <div id="el-ingreso" class="testimonio post">      
                             <h3 class="testim">El momento del ingreso</h3>
                            <p>“Mi hija estaba estudiando segundo de Bachillerato y a mí me atormentaba la idea de que tuviera que dejar el colegio. ¿Qué les diríamos a los profesores? La habían expulsado muchas veces y tampoco iba a sacar el curso… <strong>Pero la excusa de los estudios era una forma de auto engañarme, de reafirmarme en la negativa de que mi hija tuviera que ingresar en un centro</strong>. ¡¿Mi hija, ingresarse? ¡¿Están locos?!, pensaba yo. Si sólo tiene un problema de conducta. Una vez más, ¡qué equivocada estaba!<br><br>
                                Cuando llamé a Castelao, me pidieron que Isabel fuera al día siguiente a una entrevista con un chico que en aquellos días estaba en pleno proceso de recuperación. <strong>Salió de aquel encuentro con la decisión tomada: "Mamá, tengo que ingresarme"</strong>. <br>
Nunca pensé que sería capaz de traerla hasta aquí, ni siquiera de hacerle la maleta. Se me vino el mundo abajo. Creí que no sobreviviría a aquel día, el más largo y duro de mi vida. Y ya ves… Allí la dejamos su padre y yo (en el Centro de Málaga). Y allí comenzó su vida de nuevo… Y, por ende, la nuestra.” 
</p>
                        </div>
                        
                   <div id="el-estigma" class="testimonio post">         
                             <h3 class="testim">La sombra del estigma</h3>
                            <p>“A mí esto no me avergonzó nunca, aunque siempre está ahí la sombra del estigma. Si lo ocultamos en determinados círculos fue por ella; y porque más allá de nuestros íntimos, a nadie le importaba demasiado. Pero la adicción de mi hija fue algo que nunca oculté a mi circulo más cercano.<br>
                                Nuestra familia y nuestros amigos lo sabían por nosotros. Algunos, con la mejor intención, <strong>intentaban relativizar diciéndome que todos los jóvenes consumen alcohol y drogas, que es algo generalizado. Pero yo sabía que a mi hija le afectaba de una manera diferente</strong>. 
En el colegio, hablé con el tutor, aunque se lo oculté a la Dirección del centro -por si mi hija quería volver el curso siguiente-. A sus mejores amigas, mi hija les contó la verdad: les contó que ingresaba en un centro para dejar su adicción. Al resto, les dijimos que había encontrado trabajo en un hotel de Málaga. <br>
Lo más complicado ha sido hacerles entender a nuestros familiares y amigos que durante todo el proceso de recuperación, ni siquiera podemos comer o cenar fuera con nuestra hija, ni mucho menos irnos de vacaciones juntos…  Exponerla a una comida en un restaurante ante veinte mesas en las que se está consumiendo alcohol es exponerla a estímulos y riesgos que no deben asumirse en una primera fase. Pero esto no lo entiendes hasta que interiorizas que la adicción es una enfermedad, no un vicio. Poco a poco, ahora le permiten ir incorporándose a situaciones de la vida normal.”

</p>
                        </div>
                        <div id="la-recuperacion" class="testimonio post">    
                             <h3 class="testim">La recuperación</h3>
                            <p>“Desde el 15 de marzo de 2016, llevo viajando a Málaga cada quince días para ver a mi hija siempre que los terapeutas de Castelao me lo han permitido, es decir, un fin de semana de cada dos. No he faltado ni a una sola terapia de familia. Llegaba a Málaga los jueves y estaba con ella las horas que la dirección terapeútica de Castelao consideraba oportunas. <br>
Durante muchos meses no le permitieron recibir más visitas que la nuestra, la de sus padres. Luego, nos dejaron que vinieran sus hermanos, su abuela, sus tíos… Desde marzo del 16 no ha visto a ninguna de sus amigas –aunque todas le han mandado cartas, fotos y regalos durante la recuperación-. Tiene unas ganas enormes de encontrarse con ellas, claro. En julio viene la primera y se irán dosificando las visitas de las demás –a criterio de los terapeutas-, porque todas juntas no puede ser.<br>
                        Yo, que he podido visitarla desde el principio, he de decir que <strong>ha sido maravilloso ver cómo mes a mes, semana a semana, mi hija iba mejorando, la iban reeducando, la devolvían poco a poco a la vida…</strong><br>
Es cierto que esta recuperación tiene picos: unas veces me iba de aquí feliz y otras, disgustada. Pero incluso cuando la encontraba más agresiva o peor que en la visita anterior, siempre tenía la sensación de que estaba remontando. Ahora la veo extraordinariamente bien, pero ni ella ni yo queremos confiarnos. A la edad de mi hija, no se considera que un enfermo de adicción está totalmente recuperado hasta que han pasado entre 2 y 5 años sin consumir. Habiendo realizado una primera fase de la recuperación muy sólida –como parece que así ha sido-, tenemos que pensar que a partir de ahora todo va a ir bien. Aunque las dos sabemos que aún queda mucho por hacer…”

</p>
                        </div>   
                        <div id="la-culpa" class="testimonio post">    
     <h3 class="testim">La culpa</h3>
                            <p>“No he sentido culpa por no haber estado pendiente de ella en la peor fase de su adicción -que lo estuve, ¡y de qué manera!-, pero sí por la educación que le dimos. Desde muy pequeña, ella siempre tuvo un carácter muy fuerte. Le consentimos más que a sus hermanos… Muchas veces por no escucharla. Y durante un tiempo, <strong>pensé que el problema derivaba de aquella educación permisiva</strong>. Pero hoy sé que no es así, porque gracias a la gente de Instituto Castelao <strong>he ido aprendiendo que lo que tiene mi hija es una enfermedad</strong>. ¡Nadie debe sentirse culpable de que un hijo padezca cáncer! <br>
                                Ahora, transcurridos estos meses de recuperación, mi hija y yo hablamos del tema con absoluta naturalidad. ¡Te quise matar!, le comento muchas veces. <strong>Pero tampoco espero que me pida perdón porque, de haber sido diabética, nadie le hubiera exigido ese perdón</strong>.<br>
Eso sí, lo que me produce un sentimiento de culpabilidad enorme es no haberla ingresado antes, aquella primera vez que me lo recomendaron… Ahora sé que habríamos ganado un año. Y, sobre todo, le habríamos ahorrado a ella mucho sufrimiento.”

</p>
                        </div> 
                        
            <div id="agradecimientos" class="testimonio post">   

               <h3 class="testim">Agradecimientos</h3>
                            <p>“Ha pasado un año y aunque ya se lo he expresado personalmente a todas y cada una de las personas que nos han ayudado en Castelao, no quiero dejar pasar la ocasión para agradecer una vez más y valorar el esfuerzo que han hecho por nosotros. En primer lugar, quiero reconocer y agradecer el mérito que tiene mi hija que, con solo 19 años, asumió su enfermedad y ha tenido el valor suficiente para recuperarse. Y, por supuesto, agradecer a todo el equipo de Instituto Castelao su esfuerzo y su tesón, sin olvidarme de nadie –aunque si es así, espero que me perdonen-. Gracias, gracias y mil gracias a Paco, a Ernesto, a Elo, a Eva, a Lorena, a Lola, a Pablo, a José Luis, a Inma, a Nacho y a Faqui (que también me aguantaron a mi), a Maite, a Fran, a Adrián, y también a Antonio, porque esos jardines dan vida al centro… Gracias por supuesto a todos los compañeros (de enfermedad y recuperación) de mi hija, porque entre todos forman una familia sólida y solidaria. Y para terminar, quiero decir que volvería a hipotecar mi vida para conseguir lo que hemos conseguido. <br><br>
<strong>Se despide la que hoy es… ¡Una madre feliz!</strong>”
                        
</p>                  
