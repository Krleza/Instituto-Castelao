						<h2>
                                                    Sin tratamiento, superar la adiccion resulta practicamente imposible.	
						</h2>
						<p>
                                                    Un individuo que no padece la enfermedad de la adicci&oacute;n es capaz con su voluntad, de 
                                                    detener o reducir el consumo de cualquier sustancia ante los primeros síntomas de abuso.
                                                    No es así en el caso de un enfermo. Para un adicto, la voluntad no es suficiente.
                                                </p>
                                                <p>
                                                    Este hecho hace que mucha gente no enferma piense que la adicci&oacute;n puede atajarse con simple fuerza de voluntad.
                                                    Nada m&aacute;s lejos de la realidad, las características psicol&oacute;gicas y neuronales de un enfermo
                                                    suponen una barrera infranqueable a la hora de abandonar el consumo. Es por eso que ante 
                                                    la enfermedad de la adicci&oacute;n es fundamental buscar ayuda y asesoramiento exterior. Un tratamiento profesional es
                                                    capaz de recuperar al paciente haciendo que abandone el consumo y ayud&aacute;ndole a recuperar su vida.
                                                </p>
									
