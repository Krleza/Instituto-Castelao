						<h2>
                                                    Adicción a varias drogas
						</h2>
						Gran número de pacientes adictos consumen regularmente no una, sino  varias sustancias adictivas. Es muy frecuente en los últimos años la siguiente “<strong>escalera del consumo</strong>”:
                                                <ul class="list clearfix">
                                                <li>Entre los 14 y 16 años se<strong> </strong>inicia el consumo, generalmente con <strong>alcohol</strong> y/o <strong>cannabis</strong></li>
                                                <li>Entre los 17 y 20 sigue aumentando el consumo de <strong>alcohol</strong> y se incorporan sustancias estimulantes como el <strong>éxtasis</strong>. El cannabis a veces disminuye y otras se mantiene.</li>
                                                <li>Hacia los 20-25, si el poder adquisitivo del consumidor aumenta, se inicia el consumo de cocaína<strong> </strong>y este consumo va aumentando hasta desarrollar una clara <strong>adición a cocaína</strong>.</li>
                                                <li>Finalmente cuando ingresa para tratamiento, solemos encontrar un paciente adicto a la cocaína y al alcohol, consumidor  ocasional de cannabis y que, en algunas ocasiones, puede tomar fármacos tranquilizantes para “bajar” de la coca y poder dormir.</li>
                                                </ul>
                                                <p>Al final aparece una intensa e irrefrenable necesidad de consumir que pasa por encima de cualquier consideración personal, laboral o familiar.</p>
                                                <p><strong> </strong></p>
                                                <p><strong>COMPLICACIONES </strong></p>
                                                <p><strong> </strong></p>
                                                <p>La mezcla de sustancias produce una combinación y potenciación de los efectos negativos del consumo.  Mención especial merece en este cuadro la combinación frecuente con fármacos tranquilizantes, conseguidos en el mercado negro o prescritos con receta médica, alegando estados de ansiedad o insomnio que no son sino el efecto del consumo.</p>
                                                <p><strong> </strong></p>
                                                <strong>¿COMO DETECTAR EL CONSUMO</strong><strong>?</strong>
                                                <ul class="list clearfix">
                                                <li>Pérdida de interés por las actividades diarias: trabajo, estudios, familia, amigos “sanos”.</li>
                                                <li>Cambios de humor importantes, oscilando entre un estado de hiperexcitación y depresión.</li>
                                                <li>Una mayor irritabilidad o incluso con rasgos de paranoia y, a veces, agresividad.</li>
                                                <li>Disminución de la capacidad de concentración en estudios, trabajo, hijos, etc.</li>
                                                <li>Pérdida de interés por las amistades “normales” y alejamiento de los amigos no consumidores.</li>
                                                <li>Alteraciones del horario del sueño. Dormir de día y estar despierto por la noche</li>
                                                <li>Problemas de concentración con dificultades académicas y laborales.</li>
                                                <li>Pérdida de empleo y dificultades económicas por su estado, aunque el adicto mantenga que la culpa es de otros.</li>
                                                <li>Gastos injustificados  elevados derivados del consumo, prostitución, juego, etc.</li>
                                                <li>Problemas en la relación con pareja e hijos.</li>
                                                <li>Actividad descontrolada y, a la vez, cansancio crónico</li>
                                                </ul>
									
